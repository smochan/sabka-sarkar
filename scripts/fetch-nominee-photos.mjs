/**
 * Fetch CC-licensed portrait photos for seed nominees from Wikipedia/Wikimedia.
 * Writes src/content/nomineeImages.ts (name -> {image, license, attribution, sourceUrl}).
 * Only keeps images with a clearly FREE license (CC / public domain); skips the rest.
 *
 * Run: node scripts/fetch-nominee-photos.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const UA = "SabkaSarkar/1.0 (civic non-profit project; contact hello@sabkasarkar.com)";

// Extract (slug, name) pairs from profiles.ts (slug appears before name in each object).
const profilesSrc = readFileSync(join(ROOT, "src/content/profiles.ts"), "utf8");
const pairs = [];
const re = /slug:\s*"([^"]+)",\s*\n\s*name:\s*"([^"]+)"/g;
let m;
while ((m = re.exec(profilesSrc)) !== null) pairs.push({ slug: m[1], name: m[2] });
console.log(`Found ${pairs.length} nominees in profiles.ts`);

const api = "https://en.wikipedia.org/w/api.php";
const commons = "https://commons.wikimedia.org/w/api.php";

function stripHtml(s) {
  return String(s || "").replace(/<[^>]*>/g, "").replace(/\s+/g, " ").trim();
}
function isFree(licenseCode, shortName) {
  const s = `${licenseCode} ${shortName}`.toLowerCase();
  if (/fair use|non-?free|all rights reserved/.test(s)) return false;
  // CC / public-domain + open government licenses (GODL-India, OGL) all permit
  // reuse with attribution.
  return /(^|[^a-z])cc[ -]?(by|0|pd)|public domain|pd-|cc0|attribution|godl|ogl|fal|free art/.test(s);
}

// Wikipedia page titles usually omit honorifics; try a cleaned variant too.
function cleanName(name) {
  return name
    .replace(/^(Dr\.?|Prof\.?|Justice \(Retd\.\)|Justice|Pandit|Smt\.?|Shri|Mr\.?|Ms\.?)\s+/i, "")
    .trim();
}

async function getJson(url) {
  const r = await fetch(url, { headers: { "User-Agent": UA, "Api-User-Agent": UA } });
  if (!r.ok) throw new Error(`HTTP ${r.status}`);
  return r.json();
}

async function fetchPage(title) {
  const u1 = `${api}?action=query&format=json&redirects=1&prop=pageimages&piprop=thumbnail|original|name&pithumbsize=480&titles=${encodeURIComponent(title)}`;
  const j1 = await getJson(u1);
  const pages = j1?.query?.pages || {};
  const page = Object.values(pages)[0];
  return page && (page.thumbnail?.source || page.original?.source) && page.pageimage ? page : null;
}

async function fetchOne({ name }) {
  // 1) original page image + pageimage filename (try raw, then honorific-stripped)
  let page = await fetchPage(name);
  if (!page) {
    const cleaned = cleanName(name);
    if (cleaned !== name) page = await fetchPage(cleaned);
  }
  if (!page) return null;
  const image = page.thumbnail?.source || page.original.source;
  const file = `File:${page.pageimage}`;
  const sourceUrl = `https://en.wikipedia.org/wiki/${encodeURIComponent(page.title.replace(/ /g, "_"))}`;

  // 2) license + author from Commons extmetadata
  const u2 = `${commons}?action=query&format=json&prop=imageinfo&iiprop=extmetadata&titles=${encodeURIComponent(file)}`;
  const j2 = await getJson(u2);
  const cpages = j2?.query?.pages || {};
  const cpage = Object.values(cpages)[0];
  const ext = cpage?.imageinfo?.[0]?.extmetadata || {};
  const licenseCode = ext.License?.value || "";
  const shortName = ext.LicenseShortName?.value || "";
  const artist = stripHtml(ext.Artist?.value) || "Unknown";

  if (!isFree(licenseCode, shortName)) {
    return { skip: true, reason: shortName || licenseCode || "unknown license" };
  }
  return {
    image,
    imageLicense: shortName || licenseCode,
    imageAttribution: `${artist} / Wikimedia Commons (${shortName || licenseCode})`,
    sourceUrl,
  };
}

const out = {};
const skipped = [];
for (const p of pairs) {
  try {
    const res = await fetchOne(p);
    if (!res) { skipped.push(`${p.name} (no image)`); }
    else if (res.skip) { skipped.push(`${p.name} (${res.reason})`); }
    else { out[p.name] = res; console.log(`  ✓ ${p.name} — ${res.imageLicense}`); }
  } catch (e) {
    skipped.push(`${p.name} (error: ${e.message})`);
  }
  await new Promise((r) => setTimeout(r, 250)); // be polite to the API
}

const header = `// AUTO-GENERATED by scripts/fetch-nominee-photos.mjs — do not edit by hand.
// CC-licensed portraits from Wikimedia Commons, with attribution. Re-run to refresh.
import type { NomineeProfile } from "./types";

type ImageInfo = Pick<NomineeProfile, "image" | "imageAttribution" | "imageLicense" | "sourceUrl">;

export const nomineeImages: Record<string, ImageInfo> = ${JSON.stringify(out, null, 2)};
`;
writeFileSync(join(ROOT, "src/content/nomineeImages.ts"), header);
console.log(`\nWrote ${Object.keys(out).length} images; skipped ${skipped.length}:`);
skipped.forEach((s) => console.log(`  - ${s}`));
